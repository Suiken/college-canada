class Partner {
	constructor(id, name, address, city, province, postalCode, country, logo, website){
		this.id = id;
		this.name = name;
		this.address = address;
		this.city = city;
		this.province = province;
		this.postalCode = postalCode;
		this.country = country;
		this.logo = logo;
		this.website = website;
	}
}

exports.Partner = Partner;